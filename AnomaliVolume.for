	REAL FUNCTION SVAN(S,T,P0,SIGMA)
	 REAL P,T,S,SIG,SR,R1,R2,R3,R4
	 REAL A,B,C,D,E,A1,B1,AW,BW,K,K0,KW,K35
!EQUIV
	 EQUIVALENCE (E,D ,Bl),( BW ,B,R3) ,(C ,A1 ,R2)
	 EQUIVALENCE (AW ,A,R1),(KW,K0,K)
	 !DATA
	 DATA R3500,R4/1028.1063,4.8314E-4/
	 DATA DR350/28.106331/
	 !R4 IS REFERRED TO AS C IN MILLERO AND POISSON 1981
	!CONVERT PRESSURE TO BARS AND TAKE SQUARE ROOT SALINITY.
	 P=P0/10.0
	 SR = SQRT(ABS(S))
	!*********************************************************
	!PURE WATER DENSITY AT A-rnOSPHERIC PRESSURE
	!BIGG P.H. ,(1967) BR. J. APPLIED PHYSICS 8 PP 521-537.
	 Rl = ((( (6.536332E-9*T-1.120083E-6)*T+1.001685E-4)*T
	&-9. 095290E-3) *T+6. 793952E-2) *T-28.263737
	 !SEAWATER DENSITY ATM PRESS.
	 ! COEFFICIENTS INVOLVING SALINITY
	 ! R2 =A IN NOTATION OF MILLERO AND POISSON 1981
	 R2 = (((5.3875E-9*T-8. 2467E-7) *T+76438E-5) *T-4.0899E-3)*T
	&+8 .24493E-1
	 !R3 =B IN NOTATION OF MILLERO AND POISSON 1981
	 R3 = (-1.6546E-6*T+1.0227E-4)*T-5.72466E-3
	  !INTERNATIONAL ONE-ATMOSPHERE EQUATION OF STATE OF SEAWATER
	 SIG = (R4*S + R3*SR + R2)*S + R1
	 !SPECIFIC VOLUME AT ATMOSPHERIC PRESSURE
	 V350P = 1.0/R3500
	 SVA =-SIG*V350P/(R3500+SIG)
	 SIGMA=SIG+D R350
	 ! SCPlE SPECIFIC VOL. ANO~ALY TO NORMALLY REPORTED UNITS
	 SVAN=SVA*1. 0E +8
	 IF(P.EQ.0.0) RETURN
C******************************************************************
C****** NEW HIGH PRESSURE EQUATION OF STATE FOR SEAWATER ********
	 !******************************************************************
	 !
	 !
	 !MILLERO, ET AL , 1980 DSR 27A, PP 25S-264
	 !CONSTANT NOTATION FOLLOWS ARTICLE
	 !********************************************************
	 !COMPUTE COr~PRESSION TERMS
	 E =(9.1697E-10*T+2.0816E-8)*T-9.9348E-7
	 BW = (5.2787E-8*T-6.12293E-6)*T+3.47718E-5
	 B= BW + E*5
	 !

	 D= 1.91075-4
	 C=(-1.6078E-6*T-1.0981E-5)*T+2.2838E-3
	 AW = ((-5.77905E-7*T+1.16092E-4)*T+1.43713E-3)*T
	&-0.1194975
	 A= (D*SR + C)*S + AW
	 B1 = (-5.3009E-4*T+1.6483E-2)*T+7.944E-2
	 Al = ((-6.1670E-5*T+1.09987E-2)*T-0.603459)*T+54.6746
	 KW = (((-5.155288E-5*T+1.360477E-2)*T-2.327105)*T
	&+148.4206)*T-1930.06
	 K0 = (Bl*SR + A1)*S + KW
	 ! EVALUATE PRESSURE POLYNOMIAL
	 !***********************************************
	 ! KEQUALS THE SECANT BULK MODULUS OF SEAWATER
	 ! DK=K{S,T,P)-K(35,0,P)
	 ! K35=K{3S,O,P}
	 ! ***********************************************
	 DK = (B*P + A)*P + K0
	 K35 = (5.03217E-5*P+3.359406)*P+21582.27
	 GAM=P/K35
	 PK = 1.0- GAM
	 SVA= SVA*PK + (V350P+SVA)*P*DK/(K35*(K35+K0))
	 ! SCALE SPECIFIC VOL. ANOMALY TO NORMALLY REPORTED UNITS
	 SVAN=SVA*1.0E+8
	 V350P = V350P*PK
	 ! ****************************************************
	 !COMPUTE DENSITY ANOMALY WITH RESPECT TO 1000.0 KG/M**3
	 ! 1) DR350. DENSITY ANOr~LY AT 35 (PSS-78), 0 DEG. C AND 0 DECIBARS
	 ! 2) DR35P: DENSITY ANOMALY 35 (PSS-78), 0 DEG. C, PRES. VARIATION
	 ! 3) DVAN : DENSITY ANOMALY VARIATIONS INV0LVING SPECFIC VOL. ANOMALY
	 !********************************************************************
	 !CHECK VALUE: SIGMA =59.82037 KG/M**3 FOR S =40 (PSS-78),
	 !T : 40 DEG C, PO= 10000 DECIBARS.
	 !*****************************************r*************
	 DR35P=GAM/V350P
	 DVAN=SVA/(V350P*(V350P+SVA))
	 SIGMA=DR350+DR35P-DVAN
	 RETURN
	 END